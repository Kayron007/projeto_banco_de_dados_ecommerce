public class pessoa extends entidadeBase{
    private String tipo;
    
    public pessoa(){}
    
    public pessoa (int id, String tipo){
        super(id);
        this.tipo = tipo;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
}
